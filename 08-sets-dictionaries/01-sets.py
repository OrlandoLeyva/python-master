# sets: enables you to group related values.

# characteristics: unordered, unchangeable, do not allow duplicate values.

# NOTE: you can remove and add new items.

# structure: setName = {'value1', 'value2'}

# If you insert duplicate values It wil automatically remove them.
people = {'musk', 'trump', 'amlo'}

# add a new item.
people.add('samuel')

# remove item.
people.remove('amlo')
