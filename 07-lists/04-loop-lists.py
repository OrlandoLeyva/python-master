countries = ['mexico', 'italia','australia', 'canada']

# you can loop trough a list using for.

# the for loop will run its code one time for each item in the list and store the current item in the defined variable
for item in countries:
    # using the index method you can pass a value if it value exists in the array will return the index
    print(countries.index(item))
    print(item)



